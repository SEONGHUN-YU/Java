package com.yu.mar065sdi.calc;

// 멤버변수명
// 요청 parameter명하고 같아야 값 받으면서 바로 객체로 만들어 줌, 생성자들 getter setter 필수
public class XXXYYY {
	private int x;
	private int y;

	public XXXYYY() {
	}

	public XXXYYY(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
