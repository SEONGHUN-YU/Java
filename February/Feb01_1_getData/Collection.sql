create table 테이블명( -- 테이블 생성
	필드명 자료형 옵션, primary key
	필드명 자료형 옵션, not null
	...
); 
drop table 테이블명 cascade constraint purge; -- 테이블 삭제

create sequence 시퀀스명; -- 시퀀스 생성 (일반적으로 테이블명_seq)
drop sequence 시퀀스명; -- 시퀀스 삭제

select 필드명, 필드명 as 별칭, 연산자 사용가능, 통계함수 사용가능, ... -- 데이터 조회
from 테이블명, 테이블명, ...
where 조건식;
group by 필드명, 필드명, ...
having 조건식 -- group by랑 세트, group by 하고나서 보기 싫은 거 걷어낼 때 씀
order by 필드명, 필드명 desc, ...; -- 정렬 (default는 오름차순) (필드명 뒤에 desc붙이면 내림차순)

insert into 테이블명(필드명, 필드명, ...) values(값, 값, ...); -- 데이터 추가

update 테이블명 -- 데이터 수정
set 필드명 = 값, 필드명 = 값, ...
where 조건식;

